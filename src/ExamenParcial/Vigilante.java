package ExamenParcial;

public class Vigilante {
    private String nombre;
    private int dni;

    public Vigilante(String nombre, int dni) {
        this.nombre = nombre;
        this.dni = dni;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}

